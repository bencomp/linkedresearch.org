                                <h3 property="schema:name">What is this?</h3>
                                <div datatype="rdf:HTML" property="schema:description">
                                    <p>Linked Research is set out to socially and technically enable researchers to take full control, ownership, and responsibility of their knowledge, and have their contributions accessible to society at maximum capacity. Linked Research calls for dismantling the archaic and artificial barriers that get in the way of this.</p>

                                    <p>There is no central authority to judge the value of your contributions. You do not need permission to publish! Control your own research and communication. The Web already enables you to be the publisher!</p>

                                    <p>This is not something hypothetical or a dream for the future, it is completely possible today and there are ongoing initiatives and tooling to make it so.</p>

                                    <p>Linked Research’s core goals:</p>

                                    <ul>
                                        <li>Publishing and ownership of documents and data</li>
                                        <li>Discovery and reuse of knowledge and data</li>
                                        <li>User experience and tooling</li>
                                    </ul>

                                    <p>If you are an academic researcher, reconsider and question the tooling or rules that you are asked to follow e.g:</p>

                                    <ul>
                                        <li>Academic paywalls</li>
                                        <li>Information silos such as third-party publishers</li>
                                        <li>Strictly desktop or “paper user interfaces” e.g., (La)TeX, PDF/Word</li>
                                        <li>Blind reviews</li>
                                    </ul>

                                    <p>We can work towards solutions where researchers can publish and consume research objects, as well as reviews and annotations that are open and both human and machine-friendly. If you support Open Access or Open Science philosophies, you're already half way there!</p>
                                </div>
                            </section>

